#include <algorithm>
#include <iostream>
#include <set>
#include <vector>

using namespace std;

class Solution {
   public:
    vector<vector<int>> threeSum(vector<int>& nums) {
        set<vector<int>> set;
        sort(nums.begin(), nums.end());
        for (int i = 0; i < nums.size(); i++) {  // 对每个中间数搜索
            int left = i - 1;
            int right = i + 1;
            bool can_slove = false;
            for (;;) {  // 移动双指针
                if (left >= 0 && right < nums.size()) {
                    int tmp = nums.at(left) + nums.at(i) + nums.at(right);
                    if (tmp == 0) {
                        can_slove = true;
                        vector<int> ans;
                        ans.push_back(nums.at(left));
                        ans.push_back(nums.at(i));
                        ans.push_back(nums.at(right));
                        set.insert(ans);
                        while (left >= 1 && nums[left-1] == nums[left]) {
                            left--;
                        }
                        while (right < nums.size()-1 && nums[right+1] == nums[right]) {
                            right++;
                        }
                        right++;
                        left--;
                    } else {
                        if (tmp < 0) {
                            right++;
                            continue;
                        }
                        if (tmp > 0) {
                            left--;
                            continue;
                        }
                    }

                } else {
                    break;
                }
            }
        }
        return vector<vector<int>>(set.begin(), set.end());
    }
};

